package com.jspider.spring_boot_simple_crud_with_mysql.security.jwt;

import java.util.Collections;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.springframework.security.authentication.UsernamePasswordAuthenticationToken;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.test.util.ReflectionTestUtils;

import com.jspider.spring_boot_simple_crud_with_mysql.security.services.UserDetailsImpl;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertTrue;

/**
 * Stand-alone JUnit 5 unit test for the production
 * {@link JwtUtils} component.
 *
 * <h2>Testing strategy</h2>
 *
 * <p>This test class deliberately avoids {@code @SpringBootTest} and any other
 * Spring context bootstrapping. Doing so keeps the test fast (milliseconds
 * rather than several seconds for a full context load) and isolates the unit
 * under test from any auto-configuration that could mask defects in
 * {@link JwtUtils} itself. The {@code JwtUtils} instance is constructed
 * manually via its no-arg constructor and its two {@code @Value}-injected
 * private fields ({@code jwtSecret}, {@code jwtExpirationMs}) are populated
 * via {@link ReflectionTestUtils#setField(Object, String, Object)}; this
 * mirrors the canonical Spring testing idiom for unit-testing classes whose
 * only collaborators are property-resolved primitives.
 *
 * <h2>Real JJWT, not mocks</h2>
 *
 * <p>Per AAP &sect;0.5.1.4, the test exercises the REAL JJWT 0.13.x library
 * end-to-end: tokens are generated by {@code Jwts.builder()}, parsed by
 * {@code Jwts.parser()}, and signed/verified with HMAC-SHA-256 keys derived
 * from {@code Keys.hmacShaKeyFor(...)}. Mocking these primitives would
 * defeat the purpose of the test &mdash; the unit under test is the
 * cooperation between {@code JwtUtils} and JJWT, not {@code JwtUtils} in
 * isolation.
 *
 * <h2>Test secrets</h2>
 *
 * <p>Two Base64-encoded secrets are used, each decoding to 64 bytes
 * (512 bits) &mdash; well above the 32-byte (256-bit) minimum required for
 * HMAC-SHA family algorithms per RFC 7518 &sect;3.2. The 64-byte length is
 * historically chosen for both secrets so that the keys derived by
 * {@link io.jsonwebtoken.security.Keys#hmacShaKeyFor(byte[])} are at the
 * same algorithm tier; this avoids any
 * {@link io.jsonwebtoken.security.WeakKeyException} during the
 * signature-mismatch test, leaving the JJWT path to surface the genuine
 * HMAC verification failure (caught by
 * {@link JwtUtils#validateJwtToken(String)} as
 * {@code io.jsonwebtoken.security.SignatureException}). The two secrets
 * contain different byte content, so they produce different HMAC keys and
 * incompatible signatures.
 *
 * <p>The signing algorithm in the issued JWT is always {@code HS256}: as
 * of the QA-driven fix for AAP &sect;0.1.1, {@code JwtUtils.generateJwtToken}
 * passes {@link io.jsonwebtoken.Jwts.SIG#HS256} explicitly to
 * {@code .signWith(SecretKey, MacAlgorithm)}, overriding JJWT's default
 * algorithm-from-key-length auto-selection. The chosen 64-byte secret no
 * longer affects which {@code "alg"} header the token carries; it only
 * affects how many bytes of key material are used by the underlying
 * {@code Keys.hmacShaKeyFor} factory. The 64-byte length is preserved here
 * for backward compatibility of the test secret literals only.
 *
 * <h2>Cache-invalidation after reflective field mutation</h2>
 *
 * <p>Production-grade {@code JwtUtils} caches the derived
 * {@link javax.crypto.SecretKey} in a {@code signingKey} field initialized
 * by a {@link jakarta.annotation.PostConstruct @PostConstruct}-annotated
 * {@code init()} method. In a Spring-managed deployment the cache is
 * populated before any request reaches the bean, providing the AAP
 * &sect;0.7.1.4 fail-fast guarantee on weak/missing secrets. Stand-alone
 * unit tests, however, instantiate {@code JwtUtils} via
 * {@code new JwtUtils()} and never invoke {@code init()}, so the
 * {@code signingKey} field is initially {@code null}; the
 * {@link JwtUtils#key() key} accessor's lazy fallback then derives and
 * caches the key on first use.
 *
 * <p>That lazy cache must be invalidated whenever a test reflectively
 * mutates {@code jwtSecret}, otherwise the next token operation would
 * use the stale cached key derived from the previous secret. Both
 * {@link #setUp()} and {@link #validateJwtToken_invalidSignature_returnsFalse()}
 * therefore pair their {@code ReflectionTestUtils.setField("jwtSecret", ...)}
 * calls with a {@code ReflectionTestUtils.setField("signingKey", null)}
 * cache-clear so the next access re-derives the key from the current
 * {@code jwtSecret} value.
 *
 * <h2>Test method naming</h2>
 *
 * <p>All six test methods follow the
 * {@code methodUnderTest_scenario_expectedBehavior} convention so the
 * Surefire report reads as a sentence describing what is being verified.
 *
 * @see JwtUtils
 * @see UserDetailsImpl
 */
class JwtUtilsTest {

    /**
     * The unit under test. Re-instantiated by {@link #setUp()} before every
     * {@code @Test} method to guarantee per-test isolation: any field overrides
     * applied inside one test (for example, the negative {@code jwtExpirationMs}
     * used by {@link #validateJwtToken_expiredToken_returnsFalse()} or the
     * different secret used by
     * {@link #validateJwtToken_invalidSignature_returnsFalse()}) cannot leak
     * into the next test.
     */
    private JwtUtils jwtUtils;

    /**
     * Initializes a fresh {@link JwtUtils} instance and populates its two
     * {@code @Value}-injected private fields ({@code jwtSecret},
     * {@code jwtExpirationMs}) via {@link ReflectionTestUtils}.
     *
     * <p>The {@code jwtSecret} value is a Base64-encoded byte string that
     * decodes to 64 bytes &mdash; comfortably above the 32-byte minimum
     * required by HS256. The {@code jwtExpirationMs} value of 3,600,000
     * (one hour in milliseconds) gives every non-expiry test a generous
     * future {@code exp} claim.
     *
     * <p>JUnit 5 Jupiter's default
     * {@link org.junit.jupiter.api.TestInstance.Lifecycle#PER_METHOD per-method
     * lifecycle} ensures that a new instance of {@code JwtUtilsTest} is
     * created for every {@code @Test} method, which already provides
     * isolation; combining that with {@code @BeforeEach} re-creation of
     * {@link #jwtUtils} guarantees that even reflectively mutated field
     * state does not survive between tests.
     */
    @BeforeEach
    void setUp() {
        jwtUtils = new JwtUtils();
        ReflectionTestUtils.setField(jwtUtils, "jwtSecret",
                "dGVzdC1qd3Qtc2VjcmV0LWtleS1mb3ItdW5pdC10ZXN0aW5nLW9ubHktbm90LWZvci1wcm9kdWN0aW9uLXVzZQ==");
        ReflectionTestUtils.setField(jwtUtils, "jwtExpirationMs", 3600000);
        // Reset the cached signing key so JwtUtils.key() lazily re-derives it
        // from the just-assigned jwtSecret on first use. Without this, a
        // previous test that may have populated signingKey under the
        // PER_METHOD JUnit lifecycle would still be safe (a fresh JwtUtils
        // instance starts with signingKey == null), but the explicit reset
        // documents the cache contract and protects against future
        // refactors that might lift fixture state into a class-level field.
        ReflectionTestUtils.setField(jwtUtils, "signingKey", null);
    }

    /**
     * Builds a fully authenticated {@link Authentication} whose principal is
     * a {@link UserDetailsImpl} with the supplied {@code username}.
     *
     * <p>The helper produces the exact shape of {@code Authentication} that
     * {@link JwtUtils#generateJwtToken(Authentication)} expects:
     * <ul>
     *   <li>The principal is a {@code UserDetailsImpl}, which {@code JwtUtils}
     *       downcasts to read the username for the JWT {@code sub} claim.</li>
     *   <li>The credentials field is {@code null}, which is the canonical
     *       "post-authentication" value &mdash; once a user has been
     *       authenticated the password is no longer relevant.</li>
     *   <li>The authorities collection is a single-element immutable list
     *       containing {@code ROLE_USER}; the {@code ROLE_} prefix is
     *       mandatory for Spring Security's {@code hasRole(...)}
     *       expressions to evaluate correctly.</li>
     * </ul>
     *
     * <p>The 3-argument {@link UsernamePasswordAuthenticationToken}
     * constructor is used (rather than the 2-argument variant) to produce
     * a token whose {@code authenticated} flag is already {@code true};
     * the 2-argument form would yield an unauthenticated token, which is
     * incorrect for our test scenarios.
     *
     * @param username the unique login identifier to embed in the
     *                 {@code UserDetailsImpl} and ultimately in the JWT
     *                 {@code sub} claim
     * @return an {@code Authentication} suitable for passing to
     *         {@link JwtUtils#generateJwtToken(Authentication)}
     */
    private Authentication buildAuthentication(String username) {
        UserDetailsImpl userDetails = new UserDetailsImpl(
                1L,
                username,
                username + "@example.com",
                "encodedPassword",
                Collections.singletonList(new SimpleGrantedAuthority("ROLE_USER")));
        return new UsernamePasswordAuthenticationToken(userDetails, null, userDetails.getAuthorities());
    }

    /**
     * Verifies that {@link JwtUtils#generateJwtToken(Authentication)} returns
     * a non-null, non-blank string with the JWS Compact Serialization
     * structure (exactly three Base64URL-encoded segments separated by two
     * periods: {@code header.payload.signature}).
     *
     * <p>The {@code split("\\.", -1)} call uses the {@code -1} limit so that
     * trailing empty strings are preserved; this defends against the
     * pathological case of a token like {@code "a.b."} (three segments where
     * the last is empty), which a default-limit {@code split} would
     * misreport as having only two segments.
     */
    @Test
    void generateJwtToken_returnsNonNullToken() {
        Authentication auth = buildAuthentication("testuser");
        String token = jwtUtils.generateJwtToken(auth);
        assertNotNull(token, "Token must not be null");
        assertFalse(token.isBlank(), "Token must not be blank");
        // JWS Compact Serialization: header.payload.signature => exactly 3 segments separated by 2 dots
        assertEquals(3, token.split("\\.", -1).length,
                "Token must have exactly three '.' separated segments (JWS structure)");
    }

    /**
     * Round-trip test: a token produced by
     * {@link JwtUtils#generateJwtToken(Authentication)} and then validated
     * by {@link JwtUtils#validateJwtToken(String)} on the same instance must
     * pass validation.
     *
     * <p>This is the happy-path test: the secret used to sign the token and
     * the secret used to verify it are identical (because they are derived
     * from the same {@code jwtSecret} field), the signing algorithm is
     * {@code HS256} (explicitly pinned by
     * {@code Jwts.builder().signWith(SecretKey, Jwts.SIG.HS256)} per the
     * AAP &sect;0.1.1 contract), the verifying parser accepts any
     * algorithm-key combination consistent with the cached signing key,
     * and the {@code exp} claim is one hour in the future.
     */
    @Test
    void validateJwtToken_validToken_returnsTrue() {
        Authentication auth = buildAuthentication("validuser");
        String token = jwtUtils.generateJwtToken(auth);
        assertTrue(jwtUtils.validateJwtToken(token),
                "Newly generated token must validate as true");
    }

    /**
     * Verifies that {@link JwtUtils#validateJwtToken(String)} returns
     * {@code false} for an expired token.
     *
     * <p>Rather than waiting for a positive {@code jwtExpirationMs} to elapse
     * (which would require {@link Thread#sleep(long)} and introduce flaky
     * timing dependencies), the test reflectively overrides
     * {@code jwtExpirationMs} to a NEGATIVE value (-1000 ms). When
     * {@link JwtUtils#generateJwtToken(Authentication)} computes
     * {@code new Date(System.currentTimeMillis() + jwtExpirationMs)}, the
     * resulting {@code exp} claim is one second in the past, so the token
     * is born already expired.
     *
     * <p>The expected internal behavior of
     * {@link JwtUtils#validateJwtToken(String)} is to catch
     * {@link io.jsonwebtoken.ExpiredJwtException} thrown by the JJWT parser
     * and return {@code false}.
     */
    @Test
    void validateJwtToken_expiredToken_returnsFalse() {
        // Negative expiration => token born already expired, no Thread.sleep required
        ReflectionTestUtils.setField(jwtUtils, "jwtExpirationMs", -1000);
        Authentication auth = buildAuthentication("expireduser");
        String token = jwtUtils.generateJwtToken(auth);
        assertFalse(jwtUtils.validateJwtToken(token),
                "Expired token must validate as false");
    }

    /**
     * Verifies that {@link JwtUtils#validateJwtToken(String)} returns
     * {@code false} for a malformed token.
     *
     * <p>The chosen literal {@code "not.a.valid.jwt"} fails on two counts:
     * <ol>
     *   <li>It has four dot-separated segments rather than the three
     *       required by the JWS Compact Serialization format.</li>
     *   <li>None of the segments are valid Base64URL-encoded JSON; the
     *       parser cannot decode them as a JWT header or payload.</li>
     * </ol>
     *
     * <p>The expected internal behavior is to catch
     * {@link io.jsonwebtoken.MalformedJwtException} and return {@code false}.
     */
    @Test
    void validateJwtToken_malformedToken_returnsFalse() {
        String malformedToken = "not.a.valid.jwt";
        assertFalse(jwtUtils.validateJwtToken(malformedToken),
                "Malformed token must validate as false");
    }

    /**
     * Verifies that {@link JwtUtils#validateJwtToken(String)} returns
     * {@code false} when a token is presented for verification with a key
     * different from the one that signed it.
     *
     * <p>The test:
     * <ol>
     *   <li>Generates a token using the primary secret (set in
     *       {@link #setUp()}, decodes to 64 bytes / 512 bits). The signing
     *       algorithm in the token header is always {@code HS256} because
     *       {@code JwtUtils.generateJwtToken} explicitly pins
     *       {@code Jwts.SIG.HS256} on the {@code .signWith(...)} call,
     *       irrespective of the underlying secret's byte length.</li>
     *   <li>Reflectively overrides the {@code jwtSecret} field to a
     *       different 64-byte / 512-bit Base64 secret with distinct byte
     *       content. Holding both secrets at the same 64-byte length tier
     *       is preserved from the original test design: a much shorter
     *       verification key would trigger
     *       {@link io.jsonwebtoken.security.WeakKeyException} during
     *       {@link io.jsonwebtoken.security.Keys#hmacShaKeyFor(byte[])}
     *       (uncaught by {@link JwtUtils#validateJwtToken(String)}) rather
     *       than reaching the desired {@code SignatureException} path.</li>
     *   <li>Clears the cached {@code signingKey} field so the next access
     *       through {@code key()} re-derives a key from the just-assigned
     *       {@code jwtSecret} value (see the cache-invalidation note in
     *       the class-level Javadoc).</li>
     *   <li>Asks {@code validateJwtToken} to verify the previously-generated
     *       token using the new (different) key.</li>
     * </ol>
     *
     * <p>The validation step pulls the freshly-derived key from the cache
     * and HMAC verification fails because the token was signed with a
     * different key.
     *
     * <p>The expected internal behavior is to catch
     * {@code io.jsonwebtoken.security.SignatureException} and return
     * {@code false}.
     */
    @Test
    void validateJwtToken_invalidSignature_returnsFalse() {
        Authentication auth = buildAuthentication("sigtestuser");
        String token = jwtUtils.generateJwtToken(auth);
        // Switch to a different 64-byte / 512-bit Base64 secret with distinct byte content
        // (same algorithm tier as the primary secret to exercise SignatureException, not
        // WeakKeyException, on verification).
        ReflectionTestUtils.setField(jwtUtils, "jwtSecret",
                "YW5vdGhlci1zZWNyZXQta2V5LWZvci10ZXN0aW5nLXNpZ25hdHVyZS1taXNtYXRjaC1zY2VuYXJpb3Mtb25seQ==");
        // Clear the cached signing key so JwtUtils.key() re-derives from the
        // newly assigned jwtSecret. Without this clear, validation would
        // continue to use the old (cached) key and the token would
        // incorrectly verify against itself. This pairs with the AAP
        // §0.7.1.4 fail-fast caching introduced by the QA-driven fix to
        // JwtUtils: the cache makes production traffic hot-path-friendly
        // but requires explicit invalidation in tests that swap the
        // configured secret mid-fixture.
        ReflectionTestUtils.setField(jwtUtils, "signingKey", null);
        assertFalse(jwtUtils.validateJwtToken(token),
                "Token signed with a different secret must validate as false");
    }

    /**
     * Round-trip test for the {@code sub} (subject) claim. A token generated
     * for {@code username = "alice"} must, when parsed by
     * {@link JwtUtils#getUserNameFromJwtToken(String)}, return exactly
     * {@code "alice"}.
     *
     * <p>This proves that {@code JwtUtils.generateJwtToken} writes the
     * username into the {@code sub} claim and that
     * {@code JwtUtils.getUserNameFromJwtToken} extracts it without
     * mutation.
     */
    @Test
    void getUserNameFromJwtToken_returnsCorrectSubject() {
        Authentication auth = buildAuthentication("alice");
        String token = jwtUtils.generateJwtToken(auth);
        String username = jwtUtils.getUserNameFromJwtToken(token);
        assertEquals("alice", username,
                "Subject claim must equal the original username");
    }

    /**
     * Regression test for QA Issue #3 (AAP &sect;0.1.1 algorithm contract).
     *
     * <p>The JWT header's {@code "alg"} claim must always be exactly
     * {@code "HS256"}, regardless of how many bytes the operator-configured
     * {@code jwt.secret} happens to decode to. Prior to the QA-driven fix,
     * {@code JwtUtils.generateJwtToken} called the single-argument
     * {@code .signWith(SecretKey)} variant, which delegates algorithm
     * selection to JJWT 0.13's key-length heuristic and therefore upgraded
     * the algorithm to {@code HS512} for any secret &ge; 64 bytes (the
     * default {@code application.properties} secret happens to decode to
     * 69 bytes). The fix pins {@code Jwts.SIG.HS256} explicitly on the
     * two-argument {@code .signWith(SecretKey, MacAlgorithm)} variant,
     * making the header deterministic.
     *
     * <p>The verification decodes the first segment of the
     * compact-serialized JWT (the JOSE header) using the URL-safe Base64
     * alphabet (RFC 7515 mandates Base64URL without padding for JWS
     * segments), then asserts that the JSON contains the substring
     * {@code "alg":"HS256"}. This is a string-level assertion rather than
     * a JSON-AST assertion to avoid pulling in a JSON parser solely for
     * this single test; the token's header is small (typically 16-30
     * bytes after decoding) and the substring is sufficiently specific
     * that false positives are not a concern.
     */
    @Test
    void generateJwtToken_alwaysSignsWithHs256() {
        Authentication auth = buildAuthentication("alguser");
        String token = jwtUtils.generateJwtToken(auth);
        String[] parts = token.split("\\.", -1);
        assertEquals(3, parts.length,
                "Token must have exactly three '.' separated segments (JWS structure)");
        String headerJson = new String(
                java.util.Base64.getUrlDecoder().decode(parts[0]),
                java.nio.charset.StandardCharsets.UTF_8);
        assertTrue(headerJson.contains("\"alg\":\"HS256\""),
                "JWT header must declare alg=HS256 per AAP §0.1.1; was: " + headerJson);
    }
}
