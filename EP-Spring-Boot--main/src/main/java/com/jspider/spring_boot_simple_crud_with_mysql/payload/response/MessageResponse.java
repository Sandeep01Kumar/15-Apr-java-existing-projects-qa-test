package com.jspider.spring_boot_simple_crud_with_mysql.payload.response;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Outbound Data Transfer Object (DTO) carrying a single human-readable status
 * message from the authentication API and the application-wide exception
 * handler back to the HTTP client.
 *
 * <p>This DTO intentionally exposes only one field, {@code message}, so that
 * Jackson serialises any instance to a stable, predictable JSON envelope of
 * the form:
 * <pre>{@code
 *   {"message": "User registered successfully!"}
 * }</pre>
 *
 * <p><strong>Producers</strong> &mdash; the following components instantiate
 * {@code MessageResponse} for outbound responses:
 * <ul>
 *   <li>{@code AuthService.register(SignupRequest)} returns
 *       {@code new MessageResponse("User registered successfully!")} on a
 *       successful registration handled by {@code AuthController#registerUser}.</li>
 *   <li>{@code GlobalExceptionHandler} wraps most error conditions in this
 *       envelope so clients always receive an object with a {@code message}
 *       field, regardless of the underlying exception type. Examples include
 *       {@code BadCredentialsException} (HTTP 401 with
 *       {@code "Invalid username or password"}), {@code IllegalArgumentException}
 *       (HTTP 400 with the human-readable cause from
 *       {@code AuthService.register}, e.g. {@code "Error: Username is already taken!"}),
 *       JWT validation failures (HTTP 401), {@code AccessDeniedException}
 *       (HTTP 403 with {@code "Forbidden"}), and the generic-catch-all handler
 *       (HTTP 500 with {@code "An unexpected error occurred"}).</li>
 * </ul>
 *
 * <p><strong>Consumers</strong> &mdash; HTTP clients (Postman, curl, Swagger UI,
 * the Spring Boot integration tests under
 * {@code src/test/java/.../controller/AuthControllerIntegrationTest}) read the
 * {@code message} field from the response body. Jackson deserialisation in
 * tests relies on the no-args constructor generated by {@link NoArgsConstructor}
 * combined with the {@link Data}-generated setter.
 *
 * <p><strong>Lombok rationale</strong>:
 * <ul>
 *   <li>{@link Data} synthesises getter/setter pairs, {@code equals},
 *       {@code hashCode}, and {@code toString}, mirroring the Lombok-driven
 *       convention established by the existing
 *       {@code com.jspider.spring_boot_simple_crud_with_mysql.entity.Product}
 *       and {@code ...responses.ResponseStructure} classes in this codebase.</li>
 *   <li>{@link AllArgsConstructor} produces the single-argument constructor
 *       {@code public MessageResponse(String message)} that production code
 *       (e.g. {@code AuthService}, {@code GlobalExceptionHandler}) uses to
 *       instantiate the response inline with the literal message.</li>
 *   <li>{@link NoArgsConstructor} produces the parameterless constructor
 *       required by Jackson's default {@code BeanDeserializer} when an
 *       integration test deserialises a JSON response into a
 *       {@code MessageResponse} for assertion.</li>
 * </ul>
 *
 * <p>This class is intentionally a plain POJO: it carries no Spring stereotype
 * annotation ({@code @Component}, {@code @Service}, etc.) and no JPA mapping;
 * each request produces a fresh instance, making the class trivially
 * thread-safe by lack of shared mutable state.
 *
 * @see com.jspider.spring_boot_simple_crud_with_mysql.payload.response.JwtResponse
 *      for the richer login-success envelope.
 */
@Data
@AllArgsConstructor
@NoArgsConstructor
public class MessageResponse {

    /**
     * Human-readable status or error message returned to the API client. The
     * value is always populated by the producing component (success message
     * for registration, error description for failures) and serialised to the
     * JSON {@code "message"} property by Jackson.
     */
    private String message;
}
