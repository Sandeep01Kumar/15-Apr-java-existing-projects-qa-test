package com.jspider.spring_boot_simple_crud_with_mysql.entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.EnumType;
import jakarta.persistence.Enumerated;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * JPA entity representing a single security role assignable to a {@link
 * com.jspider.spring_boot_simple_crud_with_mysql.entity.User}.
 *
 * <p>Each row of the {@code roles} table corresponds to one named authority that can be
 * granted to one or more users through the {@code user_roles} join table. Within this
 * application the supported set of roles is constrained to the values defined by the
 * {@link ERole} enum &mdash; {@link ERole#ROLE_USER} and {@link ERole#ROLE_ADMIN}
 * &mdash; which are seeded into the table at application startup by the
 * {@code roleSeeder} {@code ApplicationRunner} bean declared on the application's
 * bootstrap class.
 *
 * <h2>Persistence mapping</h2>
 *
 * <ul>
 *   <li>The {@code id} field is the surrogate primary key. It is typed as
 *       {@link Integer} (not {@link Long}) because the role catalog is a small,
 *       finite enumeration set rather than an unbounded user population, and because
 *       the companion {@code RoleRepository} declares its key type as
 *       {@code JpaRepository<Role, Integer>}. The
 *       {@link GenerationType#IDENTITY} strategy delegates auto-incrementing to the
 *       underlying database column.</li>
 *   <li>The {@code name} field is the {@link ERole} value persisted as a
 *       {@code VARCHAR(20)} string via
 *       {@link Enumerated @Enumerated(EnumType.STRING)}. Persisting the canonical
 *       name (e.g., {@code "ROLE_USER"}, {@code "ROLE_ADMIN"}) rather than the enum
 *       ordinal makes the schema robust against future reordering or insertion of
 *       constants in the {@link ERole} declaration: existing rows retain their
 *       semantic meaning even if the source-level constant order changes. The
 *       {@code length = 20} constraint replaces Hibernate's default {@code 255}
 *       and is comfortably sufficient for the {@code ROLE_*} naming scheme.</li>
 * </ul>
 *
 * <h2>Lombok contract</h2>
 *
 * <p>The combination of {@link Data @Data} and {@link NoArgsConstructor
 * @NoArgsConstructor} generates, at compile time, the public no-argument constructor
 * that the JPA specification requires for every entity (used by Hibernate when
 * materializing rows into managed instances), the standard accessor and mutator pair
 * for each field, and the canonical {@code equals}, {@code hashCode}, and
 * {@code toString} implementations consistent with the existing {@code Product}
 * entity. The hand-written one-argument constructor
 * {@link #Role(ERole)} coexists with the Lombok-generated no-arg constructor and
 * provides the single-statement instantiation pattern (e.g.,
 * {@code new Role(ERole.ROLE_USER)}) used by the role-seeder bean.
 *
 * <h2>Spring Security relationship</h2>
 *
 * <p>This entity is an indirect collaborator of Spring Security: at authentication
 * time the {@code UserDetailsServiceImpl} loads a {@code User} together with its
 * associated {@code Role} set (eagerly fetched), maps each {@code Role.name} to a
 * {@code SimpleGrantedAuthority} keyed off the enum's {@code name()} string, and
 * places the resulting authority collection on the {@code Authentication} principal.
 * From that point onward the framework's {@code hasRole(...)} and
 * {@code hasAnyRole(...)} SpEL expressions in {@code @PreAuthorize} annotations on
 * controller methods evaluate against those authority strings. See {@link ERole} for
 * the complete explanation of the {@code ROLE_} prefix convention that ties this
 * persistence-layer model to the framework's expression semantics.
 *
 * @see ERole
 * @see com.jspider.spring_boot_simple_crud_with_mysql.repository.RoleRepository
 */
@Entity
@Table(name = "roles")
@Data
@NoArgsConstructor
public class Role {

    /**
     * Surrogate primary key for the role row, auto-generated by the database. Boxed
     * as {@link Integer} (rather than the primitive {@code int}) so that an
     * unsaved, transient {@code Role} instance can carry a {@code null} id; the
     * persistence provider then assigns a real value on the first {@code INSERT}.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * The named authority encoded by this role row, persisted as the enum's
     * canonical {@code name()} string in a {@code VARCHAR(20)} column. The
     * {@link EnumType#STRING} mapping is intentional and must not be changed: see
     * the class-level Javadoc and {@link ERole} for the rationale.
     */
    @Enumerated(EnumType.STRING)
    @Column(length = 20)
    private ERole name;

    /**
     * Convenience constructor for the role-seeder workflow. Used at application
     * startup by the bootstrap class's {@code ApplicationRunner} bean, which
     * persists a fresh row for each {@link ERole} constant the first time the
     * {@code roles} table is empty.
     *
     * <p>The {@code id} field deliberately remains {@code null} after construction;
     * Hibernate populates it on {@code save} via the {@code IDENTITY} generation
     * strategy declared on {@link #id}.
     *
     * @param name the {@link ERole} constant to assign to the new role row; must
     *             not be {@code null} when the entity is subsequently persisted,
     *             because the underlying column has no explicit nullability
     *             constraint but is logically required for any meaningful row.
     */
    public Role(ERole name) {
        this.name = name;
    }
}
